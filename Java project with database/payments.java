    private void combo(){
    String sql = "select * from member";
    
        try {
            pst = con.prepareStatement(sql);
            rs = pst.executeQuery();
            while(rs.next()){
                cbox.addItem(rs.getString(2));
            }
        } catch (SQLException ex) {
            Logger.getLogger(payments.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    private void payments(){
    String sql = "select * from payments";
    try {
             pst = con.prepareStatement(sql);
             rs = pst.executeQuery(sql);
             DefaultTableModel table = (DefaultTableModel)payments_table.getModel();
             table.setRowCount(0);
             while(rs.next()){
               //String age = String.valueOf(rs.getInt("age"));
             table.addRow(new String[]{rs.getString(2),rs.getString(3)});
             }
         } catch (SQLException ex) {
             Logger.getLogger(mainscreen.class.getName()).log(Level.SEVERE, null, ex);
         } 
    
    }
    private void total(){
    int num = payments_table.getRowCount();
    double Total = 0;
    for (int i = 0; i < num; i++){
    double val = Double.valueOf(payments_table.getValueAt(i, 1).toString());
    Total += val;
    }
     Tot_amount.setText(Double.toString(Total));
    }
    
