    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String value = cbox.getSelectedItem().toString();
        String Amount = cash_amount.getText();
        if(Amount.equals("")||value.equals("")){
        JOptionPane.showMessageDialog( null, "Please add Value!!" );
        
        }
        else{
         
        try {
                pst = con.prepareStatement("INSERT INTO payments(name,amount)VALUES(?,?)");
                pst.setString(1, value);
                 pst.setString(2,Amount);
         
        
         int k = pst.executeUpdate();
         if(k == 1){
             payments();
             total();
             JOptionPane.showMessageDialog( null, "Saved!" );
            cash_amount.setText("");
            
            
        }
        
            } catch (SQLException ex) {
                Logger.getLogger(registration.class.getName()).log(Level.SEVERE, null, ex);
            }
        
        }
    }//GEN-LAST:event_jButton1ActionPerformed

